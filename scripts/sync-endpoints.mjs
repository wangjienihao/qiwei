#!/usr/bin/env node

import fs from "node:fs/promises";
import path from "node:path";
import vm from "node:vm";

const DEFAULT_DOC_URL = "https://wework.apifox.cn/api-276643996";
const OUTPUT_PATH = path.resolve(process.cwd(), "src/endpoints.ts");

function decodeRemixStreamPayload(html) {
  const match = html.match(
    /window\.__remixContext\.streamController\.enqueue\("([\s\S]*?)"\);/,
  );
  if (!match) {
    throw new Error("Cannot find Remix stream payload in Apifox page.");
  }

  // Apifox serializes the stream as a JavaScript string literal.
  return vm.runInNewContext(`"${match[1]}"`);
}

function extractEndpointsFromSerializedPayload(payload) {
  const data = JSON.parse(payload);
  const endpointPattern = /^\/[a-z]+\/[a-z0-9_]+$/;
  const endpoints = new Set();

  for (const entry of data) {
    if (typeof entry !== "string") {
      continue;
    }
    if (!endpointPattern.test(entry)) {
      continue;
    }
    if (
      entry.startsWith("/api-") ||
      entry.startsWith("/doc-") ||
      entry.startsWith("/schema-")
    ) {
      continue;
    }
    endpoints.add(entry);
  }

  return Array.from(endpoints).sort((a, b) => a.localeCompare(b));
}

function buildTypeScriptSource(endpoints, docUrl) {
  const grouped = new Map();
  for (const endpoint of endpoints) {
    const parts = endpoint.split("/");
    const group = parts[1];
    if (!group) {
      continue;
    }
    if (!grouped.has(group)) {
      grouped.set(group, []);
    }
    grouped.get(group).push(endpoint);
  }

  const endpointLines = endpoints.map((item) => `  "${item}",`).join("\n");

  const groupedLines = Array.from(grouped.entries())
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([group, items]) => {
      const list = items.map((item) => `    "${item}",`).join("\n");
      return `  ${group}: [\n${list}\n  ],`;
    })
    .join("\n");

  return `/**
 * This file is auto-generated by scripts/sync-endpoints.mjs
 * Source: ${docUrl}
 */

export const ENDPOINTS = [
${endpointLines}
] as const;

export type WeworkEndpoint = (typeof ENDPOINTS)[number];

export const ENDPOINTS_BY_GROUP = {
${groupedLines}
} as const satisfies Record<string, readonly WeworkEndpoint[]>;

export type EndpointGroup = keyof typeof ENDPOINTS_BY_GROUP;
`;
}

async function main() {
  const docUrl = process.argv[2] ?? DEFAULT_DOC_URL;
  const response = await fetch(docUrl);
  if (!response.ok) {
    throw new Error(`Failed to fetch ${docUrl}, status: ${response.status}`);
  }

  const html = await response.text();
  const payload = decodeRemixStreamPayload(html);
  const endpoints = extractEndpointsFromSerializedPayload(payload);

  if (endpoints.length === 0) {
    throw new Error("No endpoint was extracted from the document.");
  }

  const source = buildTypeScriptSource(endpoints, docUrl);
  await fs.writeFile(OUTPUT_PATH, source, "utf8");

  console.log(`Generated ${endpoints.length} endpoints -> ${OUTPUT_PATH}`);
}

main().catch((error) => {
  console.error(error instanceof Error ? error.message : error);
  process.exit(1);
});
